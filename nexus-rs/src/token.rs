#[derive(Clone, Debug)]
pub enum Token {
    LeftParen,    // '('
    RightParen,   // ')'
    LeftBrace,    // '{'
    RightBrace,   // '}'
    LeftBracket,  // '['
    RightBracket, // ']'
    SemiColon,    // ';'
    Plus,         // '+'
    Minus,        // '-'
    Arrow,        // '->'
    Star,         // '*'
    Slash,        // '/'
    BackSlash,    // '\'
    Percent,      // '%'
    Comma,        // ','
    Dot,          // '.'
    Underscore,   // '_'
    Is,           // '='
    Eq,           // '=='
    Gt,           // '>'
    GtEq,         // '>='
    Lt,           // '<'
    LtEq,         // '<='
    Bang,         // '!'
    NotEq,        // '!='
    And,          // '&&'
    Or,           // '||'
    EmptyClosure, // '||' (when followed by '{')
    Pipe,         // '|'
    True,         // 'true'
    False,        // 'false'
    Let,          // 'let'
    Function,     // 'fn'
    If,           // 'if'
    For,          // 'for'
    While,        // 'while'
    Return,       // 'return'
    Print,        // 'print'
    Node,         // 'node'
    Number(f64),
    Identifier(String),
    String(String),
}
